
#!/bin/bash
#awk formating of the folder path file 
grep '.*/bin/bash$' /etc/passwd | awk 'BEGIN {
    FS=":";
    print "____________________________________________________________________________________________";
    print "| \033[1;35mUsername\033[0m            | \033[2;35mUserID\033[0m | \033[2;35mGroupID\033[36m | \033[2;35mHome\033[36m                        | \033[2;35mShell\033[0m             |";
    print "--------------------------------------------------------------------------------------------";
}
{
    printf("| \033[1;93m%-19s\033[m  | \033[32m%-6s\033[0m | \033[32m%-7s\033[0m | \033[32m%-25s\033[0m  | \033[32m%-17s\033[0m |\n", $1, $3, $4, $6, $7);
}
END {
    print "|______________________|________|_________|____________________________|___________________|";
}'
exit 0

